# Exemplo de string
texto = "Ola, mundo!"

# Fatiar do inicio até um índice específico
print(texto[:5])

# Fatiar a partir de um índice específico até o final
print(texto[6:])

#Fatiar do ínicio até o final com um passo específico
print(texto[::2])

# Fatiar com início e fim específicos
print(texto[4:9])

# Fatiar com um passo negativo para inverter uma substring
print(texto[::-1])



